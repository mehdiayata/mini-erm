    // Retourne true si le produit à déjà une transaction
    public function findIfTransaction($id)
    {
        $query = $this->createQueryBuilder('p')
            ->andWhere('p.id = :id')
            ->innerJoin('p.transaction', 't', Join::WITH, 'p.transaction = t.id')
            ->setParameter('id', $id)
            ->getQuery()
            ->getResult();

        if (empty($query)) {
            return false;
        } else {
            return true;
        }
    }

